#' @export
#'
#' @rdname PEMData
edatope <- function() {
  soilNutrient <- LETTERS[seq(1L, 5L, 1L)]
  soilMoisture <- seq(0L, 7L, 1L)
  edatopes <- paste(rep(soilNutrient, each = length(soilMoisture)),
    rep(soilMoisture, times = length(soilNutrient)), sep = '')
  stats::setNames(edatopes, edatopes)
}
